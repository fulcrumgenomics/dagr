package dagr.api

/**
  * Mix this trait in to access the URL root and version for the API.
  */
trait DagrApi {
  /** The version of the API. */
  lazy val Version: String = "v1"

  /** The root URL for the API. */
  lazy val Root: String    = "api"
}

object DagrApi {
  /** The type to use for storing a task's identifier. */
  type TaskId = Int
}
